public class Solution {
    public boolean isPalindrome(String s) {
        if (s == null || s.length() == 0) {
            return true;
        }
        int i = 0;
        int j = s.length() - 1;
        while (i < j) {
            while (i < j && !Character.isLetterOrDigit(s.charAt(i))) {
                i++;
            }
            while (i < j && !Character.isLetterOrDigit(s.charAt(j))) {
                j--;
            }
            if (i < j) {
                char c1 = Character.toLowerCase(s.charAt(i++));//note that we need to ignore cases !!!
                char c2 = Character.toLowerCase(s.charAt(j--));//note that we need to i++, j-- after comparing !!!
                if (c1 != c2) {
                    return false;
                }
            }
        }
        return true;
    }
}

